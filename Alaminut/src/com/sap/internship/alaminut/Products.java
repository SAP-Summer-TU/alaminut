package com.sap.internship.alaminut;

/**
 * Class holding information on a Products.
 */

public class Products {

	private String id;
    private String name;

    public String getId() {
        return id;
    }

    public void setId(String newId) {
        this.id = newId;
    }

    public String getName() {
        return this.name;
    }

    public void setName(String newName) {
        this.name = newName;
    } 
	
}